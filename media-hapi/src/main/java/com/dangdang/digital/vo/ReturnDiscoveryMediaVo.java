package com.dangdang.digital.vo;

public class ReturnDiscoveryMediaVo {

	public Long mediaId;
	
	public String title;
	
	public String author;
	
	public String subTitle;
	
	public String coverPic;
	
	public String descs;
	
	public String recommandWords;

	public Long getMediaId() {
		return mediaId;
	}

	public void setMediaId(Long mediaId) {
		this.mediaId = mediaId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getSubTitle() {
		return subTitle;
	}

	public void setSubTitle(String subTitle) {
		this.subTitle = subTitle;
	}

	public String getCoverPic() {
		return coverPic;
	}

	public void setCoverPic(String coverPic) {
		this.coverPic = coverPic;
	}

	public String getDescs() {
		return descs;
	}

	public void setDescs(String descs) {
		this.descs = descs;
	}

	public String getRecommandWords() {
		return recommandWords;
	}

	public void setRecommandWords(String recommandWords) {
		this.recommandWords = recommandWords;
	}
	
}
